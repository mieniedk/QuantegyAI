/**
 * Get available content domains for a grade level.
 * Returns array of { domainId, title, icon, weeks, moduleCount, lessonCount }
 */
export function getAvailableDomains(gradeId) {
  const gradeContent = COURSE_CONTENT[gradeId];
  if (!gradeContent) return [];
  return Object.entries(gradeContent).map(([domainId, domain]) => {
    const lessonCount = domain.modules.reduce((sum, m) => sum + m.lessons.length, 0);
    return {
      domainId,
      title: domain.title,
      icon: domain.icon,
      weeks: domain.weeks,
      moduleCount: domain.modules.length,
      lessonCount,
    };
  });
}

/**
 * Get all content for a specific grade and domain.
 */
export function getDomainContent(gradeId, domainId) {
  return COURSE_CONTENT[gradeId]?.[domainId] || null;
}

/**
 * Get a flat list of all lessons for a grade+domain.
 */
export function getDomainLessons(gradeId, domainId) {
  const domain = getDomainContent(gradeId, domainId);
  if (!domain) return [];
  const lessons = [];
  for (const mod of domain.modules) {
    for (const lesson of mod.lessons) {
      lessons.push({ ...lesson, moduleName: mod.title });
    }
  }
  return lessons;
}

/**
 * Count total lessons across all grades.
 */
export function getTotalLessonCount() {
  let count = 0;
  for (const gradeId of Object.keys(COURSE_CONTENT)) {
    for (const domainId of Object.keys(COURSE_CONTENT[gradeId])) {
      for (const mod of COURSE_CONTENT[gradeId][domainId].modules) {
        count += mod.lessons.length;
      }
    }
  }
  return count;
}
