/* ── Generate worksheet ── */
function generateWorksheet(selectedTeks, count, includeKey) {
  let pool = STAAR_QUESTIONS.filter(q => selectedTeks.includes(q.teks) && q.type === 'mc');
  if (pool.length === 0) pool = STAAR_QUESTIONS.filter(q => q.type === 'mc');
  const questions = shuffle(pool).slice(0, count);
  const teksLabel = selectedTeks.length <= 3 ? selectedTeks.join(', ') : `${selectedTeks.length} standards`;

  let html = `<h1>Math Worksheet</h1>
<div class="subtitle">TEKS: ${teksLabel} • ${count} Questions • Generated by Quantegy AI</div>
<div class="student-info">
  <div><label>Name</label><div class="line"></div></div>
  <div><label>Date</label><div class="line"></div></div>
  <div><label>Period</label><div class="line"></div></div>
</div>`;

  questions.forEach((q, i) => {
    html += `<div class="question"><span class="question-num">${i + 1}.</span> ${q.q}`;
    if (q.choices) {
      html += `<ul class="choices">${q.choices.map(c => `<li>${c}</li>`).join('')}</ul>`;
    } else {
      html += `<br/>Answer: <span class="answer-line"></span>`;
    }
    html += `</div>`;
  });

  html += `<div class="footer">© Quantegy AI • TEKS-aligned worksheet • Grade 3 Mathematics</div>`;

  if (includeKey) {
    html += `<div class="answer-key"><h2>Answer Key</h2>`;
    questions.forEach((q, i) => {
      const ans = Array.isArray(q.answer) ? q.answer.join(', ') : q.answer;
      html += `<div class="answer-item"><strong>${i + 1}.</strong> ${ans} — <em>${q.explanation}</em></div>`;
    });
    html += `</div>`;
  }

  openPrintWindow(html, 'Math Worksheet');
}

