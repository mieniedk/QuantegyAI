/* ── Generate progress report ── */
function generateProgressReport(className, students, grades, assignments) {
  let html = `<h1>Student Progress Report</h1>
<div class="subtitle">${className} • Generated ${new Date().toLocaleDateString()}</div>`;

  if (!students || students.length === 0) {
    html += `<p>No students found in this class.</p>`;
    openPrintWindow(html, 'Progress Report');
    return;
  }

  students.forEach(student => {
    const name = typeof student === 'string' ? student : student.name || student.id || 'Unknown';
    const studentGrades = grades.filter(g => g.studentId === name);
    const totalScore = studentGrades.reduce((s, g) => s + (g.score || 0), 0);
    const avgScore = studentGrades.length > 0 ? Math.round(totalScore / studentGrades.length) : 0;
    const barColor = avgScore >= 80 ? '#22c55e' : avgScore >= 60 ? '#eab308' : '#ef4444';

    html += `<div style="page-break-inside:avoid;margin:20px 0;padding:16px;border:1px solid #ddd;border-radius:8px">
      <h2 style="border:none;margin:0 0 8px;font-size:16px">${name}</h2>
      <div class="stat-grid">
        <div class="stat-box"><div class="val" style="color:${barColor}">${avgScore}%</div><div class="lbl">Average</div></div>
        <div class="stat-box"><div class="val">${studentGrades.length}</div><div class="lbl">Assignments</div></div>
        <div class="stat-box"><div class="val">${studentGrades.filter(g => g.score >= 70).length}</div><div class="lbl">Passing</div></div>
      </div>
      <div class="progress-bar"><div class="progress-fill" style="width:${avgScore}%;background:${barColor}"></div></div>
      <div style="font-size:11px;color:#666;margin-top:8px">
        ${avgScore >= 80 ? 'Meeting expectations — keep up the great work!' :
          avgScore >= 60 ? 'Approaching expectations — targeted practice recommended.' :
          'Below expectations — intervention and additional support needed.'}
      </div>
    </div>`;
  });

  html += `<div class="footer">© Quantegy AI • Progress Report • Confidential</div>`;
  openPrintWindow(html, 'Progress Report');
}

