// Cache for AI-translated strings
const AI_CACHE_KEY = 'allen-ace-ai-translations';
export const getAITranslationCache = () => {
  try { return JSON.parse(localStorage.getItem(AI_CACHE_KEY) || '{}'); } catch { return {}; }
};
